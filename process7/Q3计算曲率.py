# 实验数据
data = {
    '测量点': ['FBG1', 'FBG2', 'FBG3', 'FBG4', 'FBG5', 'FBG6'],
    '横坐标x': [0.0, 0.6, 1.2, 1.8, 2.4, 3.0],
    '初始状态1': [1529.000, 1529.000, 1529.000, 1529.000, 1529.000, 1529.000],
    '测试1': [1529.808, 1529.807, 1529.813, 1529.812, 1529.814, 1529.809],
    '初始状态2': [1540.000, 1540.000, 1540.000, 1540.000, 1540.000, 1540.000],
    '测试2': [1541.095, 1541.092, 1542.090, 1543.093, 1544.094, 1545.091],
    '测试1曲线曲率': [None] * 6,  # 用于填充计算后的曲率值
    '测试2曲线曲率': [None] * 6  # 用于填充计算后的曲率值
}


# 计算曲率的常数
c = 4200

# 计算曲率并填充到表格中
for i in range(len(data['测量点'])):
    # 计算测试1的曲率
    delta_lambda_test1 = data['测试1'][i] - data['初始状态1'][i]
    kappa_test1 = delta_lambda_test1 / c
    data['测试1曲线曲率'][i] = kappa_test1

    # 计算测试2的曲率
    delta_lambda_test2 = data['测试2'][i] - data['初始状态2'][i]
    kappa_test2 = delta_lambda_test2 / c
    data['测试2曲线曲率'][i] = kappa_test2

# 将表格数据格式化为易于阅读的格式
import pandas as pd

df = pd.DataFrame(data)
print(df[['测量点', '横坐标x', '测试1曲线曲率', '测试2曲线曲率']])